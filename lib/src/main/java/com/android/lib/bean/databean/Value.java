package com.android.lib.bean.databean;

//by summer on 2017-12-08.

public class Value {

    public float x;

    public float y;

    public Value(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
